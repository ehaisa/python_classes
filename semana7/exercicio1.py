# na estrutura while, é obrigatóriamente false ou true
print("Contando de 1 até 10")
i = 1
while i <= 10:
    print(i)
    i = i + 1         # a variável ficará somando até a condição se tornar falsa e o programa para
print("Fim do programa")

# toda estrutura while tem três partes: inicialização, teste e atualização